#include "TaskNode.hpp"

TaskNode::TaskNode(TaskNode *apNext, const int anTime)
{
   mpNext = std::shared_ptr<TaskNode>(apNext);
   mnTime = anTime;
}

/////////////////////////////////////////////////////////////////
// See Header File
/////////////////////////////////////////////////////////////////
void TaskNode::setNext(TaskNode *apNext)
{
   mpNext = std::shared_ptr<TaskNode>(apNext);
}

/////////////////////////////////////////////////////////////////
// See Header File
/////////////////////////////////////////////////////////////////
void TaskNode::setTime(const int anTime)
{
   mnTime = anTime;
}

/////////////////////////////////////////////////////////////////
// See Header File
/////////////////////////////////////////////////////////////////
std::shared_ptr<TaskNode> TaskNode::getNext() const
{
   return mpNext;
}

/////////////////////////////////////////////////////////////////
// See Header File
/////////////////////////////////////////////////////////////////
int TaskNode::getTime() const
{
   return mnTime;
}
